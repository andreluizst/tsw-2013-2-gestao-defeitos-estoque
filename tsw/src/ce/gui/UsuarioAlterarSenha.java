    private void jpfConfirmaNvSenhaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jpfConfirmaNvSenhaKeyReleased
        if (jpfConfirmaNvSenha.getText().compareTo(jpfNvSenha.getText()) == 0){
            lblPwdState.setText("senha OK!");
            btnOk.setEnabled(true);
        }else{
            lblPwdState.setText("senha inv√°lida!");
            btnOk.setEnabled(false);
        }
    }//GEN-LAST:event_jpfConfirmaNvSenhaKeyReleased

