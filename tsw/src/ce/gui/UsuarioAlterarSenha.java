    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        usuario= new Usuario();
        usuario.setNome(f.getUser().getNome());
        usuario.setCodUsuario(f.getUser().getCodUsuario());
        usuario.setFuncionario(f.getUser().getFuncionario());
        usuario.setPerfil(f.getUser().getPerfil());
        usuario.setSenha(jpfSenha.getText());
        try{
            f.alterarSenha(usuario, jpfConfirmaNvSenha.getText());
            doClose(RET_OK);
        }catch(GeralException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnOkActionPerformed
    
